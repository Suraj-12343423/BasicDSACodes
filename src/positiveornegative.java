import java.util.*;
public class positiveornegative {
    public static void main(String arg[]) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        System.out.println(x);
        if (x > 0) {
            System.out.println("X is positive");
        } else {
            System.out.println("X is negative");
        }
    }
}

